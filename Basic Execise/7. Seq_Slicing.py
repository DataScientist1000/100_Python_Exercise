# Question: List slicing is important in various data manipulation activities.
# Please complete the script so that it prints out the first three
# items of list letters.

# letters = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]
# Expected output:  ['a', 'b', 'c']
# --------------------------------------------------------------------------

letters = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]

print(letters[0:3])
print(letters[:3])

